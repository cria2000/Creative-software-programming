
public class WordDefinitionPair {
	private String word;
	private String definition;
	WordDefinitionPair(String wo,String def){
		word=wo;
		definition=def;
	}
	public String getWord() {
		return word;
	}
	public String getDefinition() {
		return definition;
	}
	public void setWord(String wo) {
		word=wo;
	}
	public void setDefinition(String def) {
		definition=def;
	}
}
